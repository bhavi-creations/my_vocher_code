<script>
    document.addEventListener('DOMContentLoaded', function() {
        const buttons = document.querySelectorAll('.open-get-code');

        buttons.forEach(button => {
            button.addEventListener('click', function(e) {
                e.preventDefault();

                const code = button.getAttribute('data-code');
                const description = button.getAttribute('data-description');

                const modalBody = document.getElementById('getCodeModalBody');
                modalBody.innerHTML = `<p>${description}</p><h3 style="background: #f8f9fa; padding: 10px; border: 1px solid #ddd;">${code}</h3>`;

                const getCodeModal = new bootstrap.Modal(document.getElementById('getCodeModal'));
                getCodeModal.show();
            });
        });

        // Optional: Toggle Terms & Conditions Logic
        document.querySelectorAll('.toggle-terms').forEach(function(link) {
            link.addEventListener('click', function(e) {
                e.preventDefault();
                const termsContent = this.closest('.terms_cond_styles').querySelector('.terms-content');
                termsContent.style.display = termsContent.style.display === 'none' ? 'block' : 'none';
            });
        });
    });
</script>



